  tar xzf /backup/${BACKUP_PREFIX}${TIMESTAMP}.tar.gz -C /data
EOF
        fi
        
        echo
        gum style --foreground 240 "Sauvegarde termin√©e le $(date '+%d/%m/%Y √† %H:%M:%S')"
    fi
}

