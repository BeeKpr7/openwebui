# =============================================================================

interactive_setup() {
    # En-t√™te stylis√©
    gum style \
        --foreground 212 --border-foreground 212 --border double \
        --align center --width 60 --margin "1 2" --padding "2 4" \
        "üíæ Sauvegarde OpenWebUI" "Configuration interactive"
    
    echo
    
    # 1. Chargement des variables d'environnement
    if gum confirm "Voulez-vous charger des variables d'environnement ?"; then
        ENV_TYPE=$(gum choose --header "Choisir le type d'environnement :" \
            "local (.env.local)" \
            "prod (.env.prod)" \
            "fichier personnalis√©")
        
        case "$ENV_TYPE" in
            "local (.env.local)")
                ENV_TYPE="local"
                ENV_FILE="$PROJECT_ROOT/.env.local"
                ;;
            "prod (.env.prod)")
                ENV_TYPE="prod"
                ENV_FILE="$PROJECT_ROOT/.env.prod"
                ;;
            "fichier personnalis√©")
                ENV_FILE=$(gum file --directory="$PROJECT_ROOT" --file \
                    --header "S√©lectionner le fichier d'environnement :")
                ENV_TYPE=""
                ;;
        esac
        
        # Charger imm√©diatement les variables d'environnement
        if [ -n "$ENV_FILE" ]; then
            load_env_file "$ENV_FILE"
            gum_log "info" "Variables d'environnement charg√©es depuis : $ENV_FILE"
        fi
        echo
    fi
    
    # 2. Type de sauvegarde
    BACKUP_TYPE=$(gum choose --header "Type de sauvegarde :" \
        "Locale uniquement" \
        "S3 uniquement" \
        "Locale + S3")
    
    case "$BACKUP_TYPE" in
        "S3 uniquement")
            S3_ONLY=true
            ;;
        "Locale + S3")
            S3_ONLY=false
            ;;
        *)
            S3_ONLY=false
            ;;
    esac
    echo
    
    # 3. Configuration S3 si n√©cessaire
    if [[ "$BACKUP_TYPE" == *"S3"* ]]; then
        gum style --foreground 99 --bold "üîß Configuration S3"
        echo
        
        # Utiliser les valeurs du fichier d'environnement si disponibles
        local default_bucket="${S3_BACKUP_BUCKET:-mon-bucket-backup}"
        local default_prefix="${S3_BACKUP_PREFIX:-openwebui-backups/}"
        
        S3_BUCKET=$(gum input --header "Nom du bucket S3 :" \
            --placeholder "$default_bucket" \
            --value "${S3_BACKUP_BUCKET:-}")
        
        S3_PREFIX=$(gum input --header "Pr√©fixe S3 (optionnel) :" \
            --placeholder "$default_prefix" \
            --value "${S3_BACKUP_PREFIX:-openwebui-backups/}")
        
        # S'assurer que le pr√©fixe se termine par /
        if [[ -n "$S3_PREFIX" && ! "$S3_PREFIX" =~ /$ ]]; then
            S3_PREFIX="${S3_PREFIX}/"
        fi
        echo
    fi
    
    # 4. R√©pertoire de destination (si sauvegarde locale)
    if [ "$S3_ONLY" = false ]; then
        if gum confirm "Utiliser un r√©pertoire de destination personnalis√© ?"; then
            CUSTOM_OUTPUT_DIR=$(gum file --directory \
                --header "S√©lectionner le r√©pertoire de destination :")
        fi
        echo
    fi
    
    # 5. Type de donn√©es √† sauvegarder
    DATA_TYPE=$(gum choose --header "Donn√©es √† sauvegarder :" \
        "Sauvegarde compl√®te (tous les fichiers)" \
        "Donn√©es essentielles uniquement (exclut cache, logs, mod√®les)")
    
    if [[ "$DATA_TYPE" == *"essentielles"* ]]; then
        DATA_ONLY=true
    fi
    echo
    
    # 6. R√©sum√© de la configuration
    gum style --foreground 212 --bold "üìã R√©sum√© de la configuration"
    echo
    
    gum format --type markdown << EOF
## Configuration choisie :

- **Type de sauvegarde** : $BACKUP_TYPE
- **Donn√©es** : $([ "$DATA_ONLY" = true ] && echo "Essentielles uniquement" || echo "Compl√®te")
$([ -n "$ENV_TYPE" ] && echo "- **Environnement** : $ENV_TYPE")
$([ -n "$ENV_FILE" ] && echo "- **Fichier env** : $ENV_FILE")
$([ -n "$S3_BUCKET" ] && echo "- **Bucket S3** : $S3_BUCKET")
$([ -n "$S3_PREFIX" ] && echo "- **Pr√©fixe S3** : $S3_PREFIX")
