        fi
        
        if [ "$S3_PREFIX" = "openwebui-backups/" ] && [ -n "${S3_BACKUP_PREFIX:-}" ]; then
            S3_PREFIX="$S3_BACKUP_PREFIX"
            log "Pr√©fixe S3 d√©fini depuis l'environnement : $S3_PREFIX"
        fi
    fi
    
    check_prerequisites
    create_backup
    cleanup_old_backups
    
    if [ "$QUIET_MODE" = false ]; then
        echo
        gum style \
            --foreground 212 --border-foreground 212 --border double \
            --align center --width 60 --margin "1 2" --padding "2 4" \
            "üéâ Sauvegarde termin√©e avec succ√®s !"
        
        echo
        
        if [ "$S3_ONLY" = false ]; then
            gum style --foreground 99 --bold "üìã Commandes de restauration locale :"
            local backup_path
            if [ -n "$CUSTOM_OUTPUT_DIR" ]; then
                backup_path="$CUSTOM_OUTPUT_DIR"
            else
                backup_path="$BACKUP_DIR"
            fi
            
            gum format --type code << EOF
